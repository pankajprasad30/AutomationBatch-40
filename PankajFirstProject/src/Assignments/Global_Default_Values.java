//WAP to find out default values of byte,short ,int, long,double,float,boolean,char and string data types using global variable
package Assignments;

public class Global_Default_Values 
{
	static byte a;
	static short b;
	static int c;
	static long d;
	static double e;
	static float f;
	static boolean g;
	static char h;
	static String i;
	
	public static void main(String[] args) 
	{
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
		System.out.println(h);
		System.out.println(i);
		
	}

}
